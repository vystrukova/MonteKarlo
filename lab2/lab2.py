# Изменение параметров для нового генератора
lambdaV = 100003
m = 10 ** 10
y0 = 123456789

# Переинициализация списка для сохранения сгенерированных значений
y = [y0]

# Генерация 30 случайных чисел
for i in range(1, 31):
    # Формула смешанного конгруэнтного метода
    Y = lambdaV * y[i - 1] % m
    # Вывод текущего значения (масштабированного)
    print('y' + str(i) + '=' + str(round(Y * 10 ** -10, 10)))
    # Добавление значения в список
    y.append(Y)

